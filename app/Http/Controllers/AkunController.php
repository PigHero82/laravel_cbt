    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (isset($request->password_baru)) {
            if ($request->password_baru === $request->konfirmasi_password_baru) {
                User::updatePassword(Auth::id(), $request->password_baru);

                return back()->with('success', 'Password berhasil diubah');
            } else {
                return back()->with('danger', 'Password dan Konfirmasi Password tidak sama');
            }
        }

        DataDiri::updateDataDiri($request, Auth::user()->id);

        return back()->with('success', 'Biodata berhasil diubah');
    }

