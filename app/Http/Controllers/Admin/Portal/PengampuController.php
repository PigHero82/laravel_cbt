     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $cek = ListRole::cekRole($request->id, 2);
        if ($cek != NULL) {
            return back()->with('danger', 'User telah terdaftar sebagai pengampu');
        }

        ListRole::storeRole($request->id, 2);
        return back()->with('success', 'Pengampu berhasil ditambah');
    }

